package condeSignal.constantes;

public class constantes {
    public static void main(String[] args){
        final int DAYS_IN_WEEK = 7; //Solidificamos uma constante, como gravar em uma pedra.
        System.out.println(DAYS_IN_WEEK);

    }
    
}
/* Java também fornece uma maneira de definir constantes, ou seja, variáveis que não podem alterar seu valor uma vez atribuídas. Usamos a palavra-chave "final" para declarar uma constante.A convenção de nomenclatura para constantes é usar letras maiúsculas com palavras separadas por underline _.
É uma boa prática declarar um valor como final se você souvesse que ele não mudará por vários motivos, como legibilidade, segurança (evitar modificações não intencionais) e, às vezes, desempenho.*/